<p>Існує спеціальний оператор %, за допомогою якого можна знайти залишок від ділення одного числа на інше:</p>
<?php
	echo 10 % 3; // выведет 1
?>
<br>
<?php
	echo 10 % 2; // выведет 0
?>
<br>
<?php
	$a = 10;
	$b = 3;
	echo $a % $b; // выведет 1
?>

